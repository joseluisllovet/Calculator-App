class Calculator {
    add(num1, num2) {
        return num1 + num2;
    }
    deduct(num1, num2) {
        return num1 - num2;
    }
    multiply(num1, num2) {
        return num1 * num2;
    }
    split(num1, num2) {
        return num1 / num2;
    }
    ans(num1, num2) {
        return num2;
    }
    exp(num1, num2) {
        return num1 ** num2;
    }

}